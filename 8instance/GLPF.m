function [I,J]=GLPF(handles)
I=handles.I;
J=handles.J;
I=double(I);
J=double(J);
PQ=2*size(I);
M=PQ(1);
N=PQ(2);
u=0:(M-1);
v=0:(N-1);
idx=find(u>M/2);
u(idx)=u(idx)-M;
idy=find(v>N/2);
v(idy)=v(idy)-N;
[V,U]=meshgrid(v,u);
D0=0.05*PQ(2);
H=exp(-(U.^2+V.^2)/(2*(D0.^2)));
FI=fft2(I,size(H,1),size(H,2));
FJ=fft2(J,size(H,1),size(H,2));
gi=real(ifft2(H.*FI));
gj=real(ifft2(H.*FJ));
gi=gi(1:size(I,1),1:size(I,2));
gj=gj(1:size(I,1),1:size(I,2));
I=uint8(gi);
J=uint8(gj);
